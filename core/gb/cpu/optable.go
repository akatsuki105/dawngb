func op00(c *Cpu) { /* nop */ }

func op01(c *Cpu) {
	lo := uint16(c.fetch())
	hi := uint16(c.fetch())
	c.r.bc.unpack((hi << 8) | lo)
}

func op02(c *Cpu) { c.m.Write(c.r.bc.pack(), c.r.a) }

func op03(c *Cpu) { c.r.bc.unpack(c.r.bc.pack() + 1) }

func op04(c *Cpu) { c.r.bc.hi++ }

func op05(c *Cpu) { c.r.bc.hi-- }
